

fun main(args: Array<String>) {
    val marks = 95

    if (marks >= 80) {
        println("A+")
    } else if (marks >= 70) {
        println("A")
    } else if (marks >= 60) {
        println("B")
    } else if (marks >= 50) {
        println("C")
    } else if (marks >= 45) {
        println("D")
    } else if(marks >= 40) {
        println("E")
    } else {
        println("F")
    }
}